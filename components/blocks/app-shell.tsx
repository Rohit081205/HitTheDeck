'use client'

import Image from 'next/image'
import { useState, useEffect, useRef } from 'react'
import { type Ground } from '@/lib/grounds'
import { cn } from '@/lib/utils'
import { motion, AnimatePresence } from 'framer-motion'
import { Cloud, Wind, Thermometer, Droplets, Trophy, Info, Lightbulb, MapPin, Gauge, Download, Loader2 } from 'lucide-react'
import type { WeatherData } from './main-app'
import { FinancialScoreCards } from '@/components/ui/financial-score-cards'
import { InteractivePitchModel } from '@/components/ui/3d-pitch-model'
import { PitchDecayVisualizer } from '@/components/ui/pitch-decay-visualizer'
